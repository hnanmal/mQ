class EasterEggApp:
    def __init__(self, tgt_widget):
        self.tgt_widget = tgt_widget

        # 클릭 횟수 저장 변수
        self.click_count = 0

        # 클릭 이벤트 바인딩
        self.tgt_widget.bind("<Button-1>", self.on_logo_click)

    def on_logo_click(self, event):
        """로고를 클릭할 때마다 실행"""
        self.click_count += 1

        if self.click_count == 5:
            self.show_ascii_art(
                "숨겨진 Chill Guy를 찾아내셨군요! 개발자에게 오시면 커피 한 잔 선물로 드립니다."
            )
            # self.click_count = 0  # 초기화하여 다시 찾을 수 있게 함
        elif self.click_count == 15:
            self.show_ascii_art(
                "숨겨진 Chill Guy를 또 찾아내셨군요! 당신의 근성에 박수를 보냅니다다."
            )
        elif self.click_count == 65:
            self.show_ascii_art("히익... 무.. 무서워요...")
        elif self.click_count == 165:
            self.show_ascii_art(
                "저... 물량산출은 안 하실건가요? 이제 더 없으니 그만 누르셔요.."
            )
            self.click_count = 0  # 초기화하여 다시 찾을 수 있게 함

